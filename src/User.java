// clase user

public class User{

    //atributos
    public String nombre;
    public String correo;
    public String contraseña;
    public String rol;

    //contructores
    public User(String nombre,String correo,String contraseña , String rol){
        this.nombre = nombre;
        this.correo = correo;
        this.contraseña = contraseña;
        this.rol = rol;

    }
}

